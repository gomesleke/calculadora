export function sum(n1,n2){
    return n1+n2;
};
export function subtration(n1,n2){
    return n1-n2;
};
export function multiplication(n1,n2){
    return n1*n2;
};
export function disivion(n1,n2){
    return n1/n2;
};
export function power(n1,n2){
    return n1**n2;
};
export function root(n1,n2){
    return Math.pow(n1,1/n2);
};